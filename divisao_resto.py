print('O programa realiza a divisão inteira de dois numeros e mostra o quociente e o resto')
n1=int(input('Digite o primeiro número: '))
n2=int(input('Digite o segundo número: '))
quociente=n1//n2
resto=n1%n2
print(f'A divisão inteira de {n1}//{n2} = {quociente}')
print(f'O resto da divisão inteira de {n1}%{n2} = {resto}')
